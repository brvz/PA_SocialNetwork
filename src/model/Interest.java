package model;

/**
 * Class responsible to manage and represent the Interest object.
 */
public class Interest {

    private int id;
    private String name;

    public Interest(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int identify) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
